    public void getListCauHoi(DataInputStream din, DataOutputStream dout) {
        try {
            dout.writeUTF("START");
            int n = din.readInt();
            if (n == 0) {
                JOptionPane.showMessageDialog(this, "Connect DB error", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                jButtonStart.setText("Finish");
                jButtonNext.setEnabled(true);
                for (int i=0 ;i<n;i++){
                    CauHoi a = new CauHoi();
                    a.setQuestion(din.readUTF());
                    ArrayList<String> da = new ArrayList<>();
                    for(int k =0; k<4;k++){
                        da.add(din.readUTF());
                    }
                    a.setListAnswer(da);
                    listCauHoi.add(a);
                }
                id = 0;
                addCauHoi(0);
            }
        } catch (IOException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void getKetQua(DataInputStream din, DataOutputStream dout) {
        if (clientSocket != null) {
            try {
                dout.writeUTF("FINISH");
                for( CauHoi cauHoi: listCauHoi){
                    String da = cauHoi.getIdAnswer();
                    if(da == null){
                       dout.writeUTF("");
                    }
                    else{
                        dout.writeUTF(da);
                    }
                }
                float score = din.readFloat();
                System.out.println(score);
                
            } catch (IOException ex) {
                Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }
    private void jButtonStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStartActionPerformed
        try {
            // TODO add your handling code here:
            clientSocket = new Socket("localhost", 2021);
            DataInputStream din = new DataInputStream(clientSocket.getInputStream());
            DataOutputStream dout = new DataOutputStream(clientSocket.getOutputStream());
            if (jButtonStart.getText().equalsIgnoreCase("START")) {
                getListCauHoi(din, dout);
            } else if (jButtonStart.getText().equalsIgnoreCase("FINISH")) {
                getKetQua(din, dout);
                jButtonStart.setText("Xem Lai");
            }
        } catch (IOException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButtonStartActionPerformed

