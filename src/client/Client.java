    public void addButtonIdDeThi(int n) {
        for (int i = 1; i <= n; i++) {
            String temp = i + "";
//          System.out.println(temp);
            JButton a = new JButton(temp);
            
            a.setSize(60, 50);
            a.setMargin(new Insets(10, 10, 10, 10));
            int b = i - 1;
            a.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    addCauHoi(b);
                    id = b;
                }
            });
            jPanelCauHoi.add(a);
        }
        if (n <= 50) {
            jPanelCauHoi.setLayout(new GridLayout(0, 2));
        } else {
            jPanelCauHoi.setLayout(new GridLayout(0, 5));
        }

    }
    public void addCauHoi(int n ){
        if(listCauHoi.size() != 0 && listCauHoi.size() > n){
            buttonGroup1.clearSelection();
            CauHoi cauHoi = listCauHoi.get(n);
            jLabelIdCauHoi.setText(n+1+"");
            jLabelCauHoi.setText(cauHoi.getQuestion());
            jLabelDapAnA.setText(cauHoi.getListAnswer().get(0));
            jLabelDapAnB.setText(cauHoi.getListAnswer().get(1));
            jLabelDapAnC.setText(cauHoi.getListAnswer().get(2));
            jLabelDapAnD.setText(cauHoi.getListAnswer().get(3));
            if(cauHoi.getIdAnswer() != null){
                switch(cauHoi.getIdAnswer()){
                    case "A":{
                        jRadioButtonA.setSelected(true);
                        break;
                    }
                    case "B":{
                        jRadioButtonB.setSelected(true);
                        break;
                    }
                    case "C":{
                        jRadioButtonC.setSelected(true);
                        break;
                    }
                    case "D":{
                        jRadioButtonD.setSelected(true);
                        break;
                    }
                    
                }
            }
            
        }
    }
